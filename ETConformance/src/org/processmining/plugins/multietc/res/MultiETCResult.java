package org.processmining.plugins.multietc.res;

import java.util.HashMap;
import java.util.Map;

public class MultiETCResult {
	
	public static final String ETC_SETT = "ETC Settings";
	
	public static final String AUTO_STATES = "Automaton States";
	public static final String AUTO_STATES_IN = "Automaton States IN";
	public static final String AUTO_STATES_OUT = "Automaton States OUT";
	
	public static final String AUTO_STATES_BACK = "Backwards Automaton States";
	public static final String AUTO_STATES_IN_BACK = " Backwards Automaton States IN";
	public static final String AUTO_STATES_OUT_BACK = "Backwards Automaton States OUT";
	
	public static final String PRECISION = "Precision";
	public static final String BACK_PRECISION = "Backwards Precision";
	public static final String BALANCED_PRECISION = "Balanced Precision";

	private Map<Object,Object> attr;
	
	
	public MultiETCResult(){
		attr = new HashMap<Object,Object>();
	}
 
	public Object putAttribute(Object key, Object value){
		return attr.put(key, value);
	}
	
	public Object getAttribute(Object key){
		return attr.get(key);
	}

}
