package org.processmining.plugins.petrinet.replayresult;

/**
 * @author aadrians
 * 
 *         Identified steps from log replay with cost
 */
public enum StepTypes {
	LMGOOD("Sync move"), //
	LMNOGOOD("False sync move"), //
	L("Log move"), //
	MINVI("Invisible step"), //
	MREAL("Model move"), //
	LMREPLACED("Replaced step"), //
	LMSWAPPED("Swapped step");
	private final String name;

	StepTypes(String name) {
		this.name = name;
	}

	public String toString() {
		return name;
	}
}
